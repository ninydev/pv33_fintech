import crypto from 'node:crypto';

/**
 * ============================================================================
 * БЛОК БЛОКЧЕЙНА (BLOCK)
 * ============================================================================
 * 
 * Класс Block описывает базовую структурную единицу распределенного реестра.
 * Блокчейн состоит из последовательности таких блоков, жестко связанных между 
 * собой криптографическими хэшами.
 * 
 * Зачем нужен этот класс:
 * 1. Хранение полезной нагрузки (данных/транзакций), которые мы хотим
 *    неизменяемо зафиксировать в сети.
 * 2. Хранение метаданных (индекс, временная метка, nonce).
 * 3. Обеспечение криптографической связности - каждый блок хранит хэш 
 *    предыдущего блока (previousHash), вычисляя на основе всех своих данных
 *    и хэша предшественника собственный уникальный хэш (hash).
 * 
 * Любое изменение данных внутри уже существующего блока приведет к изменению 
 * его хэша, что автоматически сделает невалидными все последующие блоки 
 * в цепочке. Это гарантирует неизменяемость и безопасность всей системы.
 * ============================================================================
 */
export default class Block {
  /**
   * @param {number} index Индекс блока в цепочке.
   * @param {any} data Данные, хранящиеся в блоке.
   * @param {string} previousHash Хэш предыдущего блока в цепочке.
   */
  constructor(index, data, previousHash = '') {
    /**
     * @type {number} Индекс блока в цепочке.
     */
    this.index = index;
    /**
     * @type {string} Временная метка создания блока.
     */
    this.timestamp = new Date().toISOString();
    /**
     * @type {any} Данные, хранящиеся в блоке.
     */
    this.data = data;
    /**
     * @type {string} Хэш предыдущего блока в цепочке.
     */
    this.previousHash = previousHash;
    /**
     * @type {string} Хэш текущего блока.
     */
    this.hash = this.calculateHash();
    /**
     * @type {number} Случайное число, используемое в процессе майнинга.
     */
    this.nonce = 0;
  }

  /**
   * Рассчитывает хэш блока на основе его свойств.
   * @returns {string} Хэш блока в виде шестнадцатеричной строки.
   */
  calculateHash() {
    return crypto
      .createHash('sha256')
      .update(
        this.index +
          this.previousHash +
          this.timestamp +
          JSON.stringify(this.data) +
          this.nonce
      )
      .digest('hex');
  }
}